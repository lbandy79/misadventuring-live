// ─── Character definition ──────────────────────────────────────────────
export interface DecoderRingCharacter {
  id: string;                 // e.g. "shell", "cowl", "reptar"
  year: number;
  name: string;               // In-world name
  sourceCereal: string;       // Real-world cereal
  sourceIP: string;           // Real-world IP
  manufacturer: string;
  cerealYears: string;        // e.g. "1989–1995"
  era: Era;
  flicker: FlickerRating;
  image: string;              // filename in /assets/cereal-boxes/
  oneLiner: string;           // Quick intro for reveal
  concept: string;            // 1–2 sentence character pitch (DM reference)
  bestRole: ShipRole;         // Suggested best-fit role
  altRoles: ShipRole[];       // Other viable roles
  nostalgiaHook: string;      // What the audience will recognize
  isNPC: boolean;             // true = Pebblestones, Founder (don't join crew)
  npcNote?: string;           // Why they don't join
}

