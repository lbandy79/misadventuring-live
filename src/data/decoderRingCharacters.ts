// ─── Helper functions ──────────────────────────────────────────────────

export function getCharacterByYear(year: number): DecoderRingCharacter[] {
  return DECODER_RING_CHARACTERS.filter(c => c.year === year);
}

export function getCharacterById(id: string): DecoderRingCharacter | undefined {
  return DECODER_RING_CHARACTERS.find(c => c.id === id);
}

export function getCrewableCharacters(): DecoderRingCharacter[] {
  return DECODER_RING_CHARACTERS.filter(c => !c.isNPC);
}

export function getCharactersByEra(era: Era): DecoderRingCharacter[] {
  return DECODER_RING_CHARACTERS.filter(c => c.era === era);
}

export function getUniqueYears(): number[] {
  return [...new Set(DECODER_RING_CHARACTERS.map(c => c.year))].sort((a, b) => a - b);
}

/** Get flicker CSS class for visual instability effect */
export function getFlickerClass(flicker: FlickerRating): string {
  const map: Record<FlickerRating, string> = {
    none: '',
    very_low: 'flicker-vlow',
    low: 'flicker-low',
    medium: 'flicker-med',
    high: 'flicker-high',
    extreme: 'flicker-extreme',
  };
  return map[flicker];
}
